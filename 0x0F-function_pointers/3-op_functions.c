/**
 * op_add - sum of a and b
 * @a: first operand
 * @b: second operand
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract of a and b
 * @a: first operand
 * @b: second operand
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply of a and b
 * @a: first operand
 * @b: second operand
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide of a and b
 * @a: first operand
 * @b: second operand
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module of a and b
 * @a: first operand
 * @b: second operand
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
